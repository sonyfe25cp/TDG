public class SellerHelpService {
	private int id = 0;
	private String aboutUs;
	private String businessProcess;
	private String companyService;
	private String contactUs;
	private String joinUs;
	private String platformHelp;
	private int isChinese = 1;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getAboutUs() {
		return aboutUs;
	}
	public void setAboutUs(String aboutUs) {
		this.aboutUs = aboutUs;
	}
	public String getBusinessProcess() {
		return businessProcess;
	}
	public void setBusinessProcess(String businessProcess) {
		this.businessProcess = businessProcess;
	}
	public String getCompanyService() {
		return companyService;
	}
	public void setCompanyService(String companyService) {
		this.companyService = companyService;
	}
	public String getContactUs() {
		return contactUs;
	}
	public void setContactUs(String contactUs) {
		this.contactUs = contactUs;
	}
	public String getJoinUs() {
		return joinUs;
	}
	public void setJoinUs(String joinUs) {
		this.joinUs = joinUs;
	}
	public int getIsChinese() {
		return isChinese;
	}
	public void setIsChinese(int isChinese) {
		this.isChinese = isChinese;
	}
	public String getPlatformHelp() {
		return platformHelp;
	}
	public void setPlatformHelp(String platformHelp) {
		this.platformHelp = platformHelp;
	}
	
	

}
